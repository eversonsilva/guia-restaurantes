<?php

function soma($x, $y) {
    $soma = $x + $y;
    return $soma;
}

function subtracao($x, $y) {
    $subtracao = $x - $y;
    return $subtracao;
}

function multiplicacao($x,$y){
    $multiplicacao = $x * $y;
    return $multiplicacao;
}

function divisao($x,$y){
    $divisao = $x/$y;
    return $divisao;
}
?>